import '../models/spirit_type.dart';

/// 精灵提示词配置文件
///
/// 包含每个精灵的 system prompt，用于保持 Flutter 版本和 iOS 版本的 AI 回复风格一致。
///
/// 使用规则：
/// - 如果 isGroupChat = true，使用群聊模式的 prompt
/// - 如果 isGroupChat = false 且 spiritType != null，使用对应精灵的 prompt
/// - 如果 isGroupChat = false 且 spiritType == null，使用群聊模式的 prompt（默认）
///
/// 参考 iOS 代码：ScheduleApp/Views/AI/AIAPIManager.swift
/// 所有提示词都从 iOS 代码中完整复制，保持一致性。
class SpiritPrompts {
  SpiritPrompts._(); // 私有构造函数，防止实例化

  /// 获取系统提示词
  ///
  /// [spiritType] 精灵类型，null 表示群聊模式
  /// [isGroupChat] 是否为群聊模式
  ///
  /// 返回对应的 system prompt 文本
  static String getSystemPrompt(SpiritType? spiritType, bool isGroupChat) {
    // 群聊模式
    if (isGroupChat || spiritType == null) {
      return _getGroupChatPrompt();
    }

    // 私聊模式：根据精灵类型返回对应的 prompt
    switch (spiritType) {
      case SpiritType.light:
        return _getLightSpiritPrompt();
      case SpiritType.water:
        return _getWaterSpiritPrompt();
      case SpiritType.soil:
        return _getSoilSpiritPrompt();
      case SpiritType.air:
        return _getAirSpiritPrompt();
      case SpiritType.nutrition:
        return _getNutritionSpiritPrompt();
    }
  }

